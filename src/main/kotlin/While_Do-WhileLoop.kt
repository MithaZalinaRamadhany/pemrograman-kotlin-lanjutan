fun main(args: Array<String>) {
    //While loop
    var x:Int = 0
    println("Example of while Loop--")
    while (x <= 10){
        print("$x ")
        x++
    }
    print('\n')


    //Do-While loop
    var y:Int = 0
    do{
        y = y + 10
        println("I am inside Do Block--" +y)
    }while (y <= 50)
}